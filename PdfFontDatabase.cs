        public static PdfFontMetrics GetFont(string font_family, bool is_bold = false, bool is_italic = false)
        {
            return Database.FirstOrDefault(x =>
                (x.FamilyName == font_family) &&
                (x.IsBold == is_bold) &&
                (x.IsItalic == is_italic));
        }

